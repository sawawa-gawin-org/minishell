#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	i;
	size_t	len;

	i = 0;
	len = ft_strlen(s);
	while (i <= len)
	{
		if ((unsigned char) s[i] == (unsigned char) c)
			return ((char *)(s + i));
		i ++;
	}
	return (NULL);
}
